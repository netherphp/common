<?php

namespace Nether\Common\Error;
use Nether\Common;

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

#[Common\Meta\Date('2025-02-16')]
class ConfigNotFound
extends Common\Error {

	public readonly string
	$Key;

	////////////////////////////////////////////////////////////////
	////////////////////////////////////////////////////////////////

	public function
	__Construct(string $Key) {
		parent::__Construct("config not found: {$Key}");

		$this->Key = $Key;

		return;
	}

};
