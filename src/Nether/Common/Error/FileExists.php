<?php

namespace Nether\Common\Error;
use Nether\Common;

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

#[Common\Meta\Date('2023-11-16')]
class FileExists
extends Common\Error {

	public readonly string
	$Filename;

	////////////////////////////////////////////////////////////////
	////////////////////////////////////////////////////////////////

	public function
	__Construct(string $Filename) {
		parent::__Construct("file already exists: {$Filename}");

		$this->Filename = $Filename;

		return;
	}

};
